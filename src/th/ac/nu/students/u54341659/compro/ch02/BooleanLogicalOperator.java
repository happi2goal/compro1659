public class BooleanLogicalOperator {
    
    public static void main(String[] args) {
        boolean t = true, f = false;
        
        System.out.println(t & f);
        System.out.println(t | f);
        System.out.println(t ^ f);
        System.out.println(!t); 
    
    }
}
